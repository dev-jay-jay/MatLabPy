x = 10
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)

print(x > y)
print(x < y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)

# Assignment Operators
z = 7
print(z)

z += 2
print(z)

y -= 4
print(y)

# Logical operators

print(14 > 12 and 6 > 4)
print(14 < 4 or 9 < 12)
print(not (14 < 12 or 6 < 4))
print(not(14 > 12 and 6 > 4))
